<b>Artist: Patricia Rendulic</b><br/>
<br/>
Patricia has been working with glass as an art form for nearly two decades.  She tried many styles of glass art and found herself most intrigued by a 15th century technique and made it her goal to recreate this art form.   She has gone to great lengths to recapture this centuries old method that incorporates glass with metals such as gold, silver, copper, and bronze. <br/>
<br/>
She spent 10 years studying the various processes and practiced through trial and error.  The stains she uses are made using old recipes and ingredients to mimic the extraordinary staying power used by the masters of the past.  She uses pure pounded metals and metal alloys.  Many times she incorporates historic motifs acquired from her long years of study.<br/>
<br/>
She loves the difficulties and challenges this art form takes on but creates a final product that seems to have just fallen into place.  Each piece incorporates as many as fourteen steps and thus takes many hours from design to completion.<br/>
<br/>
<br/>
